export const getDuration = (number: number) => {
  switch (number) {
    case 0:
      return "duration-600";
    case 1:
      return "duration-900";
    case 2:
      return "duration-1250";
    case 3:
      return "duration-600";
    case 4:
      return "duration-900";
    case 5:
      return "duration-1250";
    case 6:
      return "duration-600";
    case 7:
      return "duration-900";
    case 8:
      return "duration-1250";

    default:
      return "duration-600";
  }
};
